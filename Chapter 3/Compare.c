#include <stdio.h>

void main()
{
    int x, y;
   printf("input x:"); scanf_s("%d", &x);
   printf("input y:"); scanf_s("%d", &y);
   printf("x==y result :%d\n", x==y);
   printf("x!=y result :%d\n", x != y);
   printf("x>y result :%d\n", x > y);
   printf("x<y result :%d\n",x < y);
   printf("x>=y result :%d\n",x >= y);
   printf("x<=y result :%d\n",x <= y);
}
